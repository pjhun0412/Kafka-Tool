export type LanguagePreference = "auto" | "ko" | "en";
export type AppLanguage = "ko" | "en";

export function normalizeLanguagePreference(value: unknown): LanguagePreference {
  return value === "ko" || value === "en" || value === "auto" ? value : "auto";
}

export function getSystemLanguage(): AppLanguage {
  const locale = (navigator.language || "").toLowerCase();
  return locale.startsWith("ko") ? "ko" : "en";
}

export function resolveLanguage(preference: LanguagePreference): AppLanguage {
  return preference === "auto" ? getSystemLanguage() : preference;
}

