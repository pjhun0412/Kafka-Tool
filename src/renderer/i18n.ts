function applyParams(template: string, params: Record<string, string>) {
  return template.replace(/\{(\w+)\}/g, (_match, key: string) => params[key] ?? "");
}

export type TranslationKey = keyof typeof messages.en;

export function t(language: AppLanguage, key: TranslationKey, params: Record<string, string> = {}) {
  return applyParams(messages[language][key] ?? messages.en[key], params);
}

export function translateMessage(language: AppLanguage, message: string) {
  const key = messageKeysByText[message];
  if (key) return t(language, key);

  const copied = /^Copied (\d+) topic name\(s\)\.$/.exec(message);
  if (copied) return t(language, "toast.copiedTopicNames", { count: copied[1] });

  const deleted = /^Deleted (\d+) topic\(s\)\.$/.exec(message);
  if (deleted) return t(language, "toast.deletedTopics", { count: deleted[1] });

  const purged = /^Purged (\d+) topic\(s\)\.$/.exec(message);
  if (purged) return t(language, "toast.purgedTopics", { count: purged[1] });

  const connecting = /^Connecting to (.+)\.\.\.$/.exec(message);
  if (connecting) return t(language, "toast.connectingServer", { server: connecting[1] });

  const sent = /^Message sent: (.+)$/.exec(message);
  if (sent) return t(language, "toast.messageSent", { result: sent[1] });

  const downloadingVersion = /^Downloading new version (.+)\.\.\.$/.exec(message);
  if (downloadingVersion) return t(language, "update.downloadingVersion", { version: downloadingVersion[1] });

  const latest = /^You are on the latest version\. \((.+)\)$/.exec(message);
  if (latest) return t(language, "update.latest", { version: latest[1] });

  const downloadingProgress = /^Downloading update (\d+)%$/.exec(message);
  if (downloadingProgress) return t(language, "update.downloadingProgress", { percent: downloadingProgress[1] });

  const downloaded = /^Update (.+) downloaded$/.exec(message);
  if (downloaded) return t(language, "update.downloaded", { version: downloaded[1] });

  const exportedWithPath = /^Settings export completed: (.+)$/.exec(message);
  if (exportedWithPath) return `${t(language, "toast.settingsExportCompleted")}: ${exportedWithPath[1]}`;

  const recoveredEnglish = /^(.+) server connection recovered\.$/.exec(message);
  if (recoveredEnglish) return t(language, "toast.serverRecovered", { server: recoveredEnglish[1] });

  const disconnectedEnglish = /^(.+) server connection lost(?:[:.].*)?$/.exec(message);
  if (disconnectedEnglish) return t(language, "toast.serverDisconnected", { server: disconnectedEnglish[1] });

  const recovered = /^(.+) 서버 연결이 복구되었습니다\.$/.exec(message);
  if (recovered) return t(language, "toast.serverRecovered", { server: recovered[1] });

  const disconnected = /^(.+) 서버 연결이 끊겼습니다\.$/.exec(message);
  if (disconnected) return t(language, "toast.serverDisconnected", { server: disconnected[1] });

  return message;
}
